package re03;

public class SwitchEx03 {
	public static void main(String[] args) {
		int a = 10;
		switch(a) {
			case 10:
				System.out.println("10점");
			break;
			case 9:
				System.out.println("9점");
			break;
			case 8:
				System.out.println("8점");
			default:
				System.out.println("0점");
		}
	}
}
