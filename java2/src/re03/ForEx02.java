package re03;

public class ForEx02 {
	public static void main(String[] args) {
		for(int a=1; a<=3; a++) {
			System.out.println(a);
		}
		
		System.out.println("-----------");
		
		// 0~7까지 출력하는 코드
		for(int b=0; b<=7; b++) {
			System.out.println(b);
		}
		
		System.out.println("-----------");
		
		// 1~50까지 출력하는 코드
		for(int c=1; c<51; c++) {
			System.out.println(c);
		}
	}
}
